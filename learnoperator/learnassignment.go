package learnoperator

import "fmt"

//--------------------- 赋值运算符 --------------
/*
	=    简单的赋值运算符，将一个表达式的值赋给一个左值
	+=   相加后再赋值
	-=   相减后再赋值
	*=   相乘后再赋值
	/=   相除后再赋值
	%=   求余后再赋值
	<<=  左移后再赋值
	>>=  右移后再赋值
	&=   按位与后再赋值
	|=   按位或后再赋值
	^=   按位异或后再赋值
 */

func PrintAssignment(){
	var x = 10
	x += 2
	x |= 2
	fmt.Println(x)
}